func Distance(a, b uint64) int8 {
	d := a ^ b
	buf := make([]byte, 8)
	binary.PutUvarint(buf, d)
	dist := int8(0)
	for _, v := range buf {
		dist += byteDist[v]
	}
	return dist
}

